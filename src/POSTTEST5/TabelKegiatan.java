    private void jupdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jupdateActionPerformed
        if(jnama.getText().length()==0 || jnotelepon.getText().length()==0) {
            JOptionPane.showMessageDialog(null, "Nama dan Nomor Telepon Harus Di isi !");
        }
        else{
        int baris = 0;
        // TODO add your handling code here:
        jTable1.setValueAt(jnama.getText(), baris, 0);
        jTable1.setValueAt(jnotelepon.getText(), baris, 1);
        jTable1.setValueAt(jkotasal.getSelectedItem(), baris, 2);}
    }//GEN-LAST:event_jupdateActionPerformed

    private void btnTambahActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTambahActionPerformed
        // TODO add your handling code here:
        if(jnama.getText().length()==0 || jnotelepon.getText().length()==0) {
            JOptionPane.showMessageDialog(null, "Nama dan Nomor Telepon Harus Di isi !");
        }
        else{
        Object[] data = new Object[3]; //sesuaikan dengan jumlah kolom
        data[0] = jnama.getText();
        data[1] = jnotelepon.getText();
        data[2] = jkotasal.getSelectedItem().toString();

        model.addRow(data);
        }
    }//GEN-LAST:event_btnTambahActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        // TODO add your handling code here:
        int baris = jTable1.getSelectedRow();

        String nim_edit = jTable1.getValueAt(baris, 0).toString();
        String nama_edit = jTable1.getValueAt(baris, 1).toString();
        String angkatan_edit = jTable1.getValueAt(baris, 2).toString();

        jnama.setText(nim_edit);
        jnotelepon.setText(nama_edit);
        jkotasal.setSelectedItem(angkatan_edit);
    }//GEN-LAST:event_jTable1MouseClicked

