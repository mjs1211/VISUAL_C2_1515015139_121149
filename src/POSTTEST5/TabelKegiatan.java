    private void jnoteleponKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jnoteleponKeyPressed
        // TODO add your handling code here:
        jnotelepon.addKeyListener(new KeyAdapter() {
            public void keyTyped(KeyEvent e) {
            char character = e.getKeyChar();
            if(((character <'0') || (character >'9')) && character != '\b') {
                e.consume();
            }
            }
        });
    }//GEN-LAST:event_jnoteleponKeyPressed

